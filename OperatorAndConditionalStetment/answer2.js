let score = prompt("Enter the score of the student");

if (score >= 80) {
    console.log("A Grade");
}
else if (score >= 70) {
    console.log("B Grade");
}
else if (score >= 60) {
    console.log("C Grade");
}
else if (score >= 50) {
    console.log("D Grade");
}
else {
    console.log("Fail");
}
